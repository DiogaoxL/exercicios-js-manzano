// Calcular e apresentar o valor do volume de uma lata de óleo, utilizando a fórmula:
// Volume= pi * Raio * Altura

var raio = prompt("digite o raio da lata: ");
var altura = prompt("digite a altura da lata: ");


var volume = Math.PI * raio * raio * altura ; 

alert("O valor do volume é: " + volume);
