// laborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à 
// soma dos quadrados dos três valores lidos

var a = parseInt(prompt("digite um numero: "))
var b = parseInt(prompt("digite outro numero: "))
var c = parseInt(prompt("digite ultimo numero: "))

var resultado = (a*a) + (b*b) + (c*c)

alert("a soma dos quadrados dos três valores lidos é: " + resultado)