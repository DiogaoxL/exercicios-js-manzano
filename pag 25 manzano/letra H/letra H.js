// laborar um programa que calcule e apresente o volume de uma caixa retangular, por meio da fórmula 
// VOLUME = COMPRIMENTO * LARGURA * ALTURA.

var comprimento = prompt("qual o comprimento da caixa retangular: ")
var largura = prompt("qual o largura da caixa retangular: ") 
var altura = prompt("qual a altura da caixa retangular: ")


var volume = comprimento * largura * altura

alert("o volume da caixa retangular é " + volume)

